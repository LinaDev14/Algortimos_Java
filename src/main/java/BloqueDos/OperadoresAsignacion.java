package BloqueDos;

public class OperadoresAsignacion {
    public static void main(String[] args) {

        int numero = 10;

        //sumar
        numero +=5; // numero = numero + 5

        //restar
        numero-=4;

        //mult
        numero *=3;

        //div
        numero /=2;

        //modulo
        numero %= 4;


        System.out.println("numero = " + numero);
    }
}
