package BloqueUno;

public class DatosNoPrimivitos {

    // No primitivos ->
    public static void main(String[] args) {

        // además de almacenar números enteros, también puedo almacenar null
        Integer numero = null;
        System.out.println("numero = " + numero);

        // Los datos no primitivos(objetos) tienen métodos
        String palabra = "Hola que tal";
        System.out.println("palabra = " + palabra);
    }
}
